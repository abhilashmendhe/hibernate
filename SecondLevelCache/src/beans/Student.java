package beans;

public class Student {

	private int id,marks;
	private String name;
	private String email;
	
	public Student() {}

	public Student(int id, int marks, String name, String email) {
		super();
		this.id = id;
		this.marks = marks;
		this.name = name;
		this.email = email;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getMarks() {
		return marks;
	}

	public void setMarks(int marks) {
		this.marks = marks;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getEmail() {
		return email;
	}

	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
